// archiveLevelSupportNoticeFormat is the one-time stderr notice emitted when
// generating a fresh managed-server config.yaml against an external dolt
// that predates auto_gc_behavior.archive_level support. Never fires for an
// existing config.yaml (ensureProxiedServerConfig only renders on first
// bootstrap), so this is not a per-invocation warning.
const archiveLevelSupportNoticeFormat = "Info: external dolt predates archive_level config support (need >= Dolt %s); " +
	"this managed server's background auto-GC may still produce zstd archives.\n"

// managedProxiedServerConfigMarker is written as a YAML comment header on
// every config.yaml Beads itself generates (renderProxiedServerConfig).
// YAML comments are ignored by Dolt's own parser, so this is invisible to
// dolt sql-server; Beads uses it to tell its own config.yaml apart from a
// user-supplied one (env var / sidecar custom path, or a default-path file
// pre-dating this marker) before deciding whether it is safe to
// automatically reconcile auto_gc_behavior.archive_level on load
// (reconcileManagedProxiedServerConfig) versus leaving the file untouched
// and only warning (warnUnmanagedProxiedServerConfig). See
// gastownhall/beads#4986.
const managedProxiedServerConfigMarker = "# Managed by Beads - safe to delete (will be regenerated). " +
	"Hand edits may be lost on upgrade. See gastownhall/beads#4986.\n"

// isManagedProxiedServerConfig reports whether body was generated by
// renderProxiedServerConfig (carries managedProxiedServerConfigMarker as
// its first line) rather than hand-written or supplied by a user.
func isManagedProxiedServerConfig(body []byte) bool {
	return bytes.HasPrefix(body, []byte(managedProxiedServerConfigMarker))
}

// reconcileManagedProxiedServerConfig parse-modify-rewrites a Beads-managed
// config.yaml's auto_gc_behavior.archive_level so it matches the resolved
// external dolt's capability, preserving every other field (including
// port, and sibling auto_gc_behavior fields such as a user-set "enable")
// by round tripping through the servercfg YAML structs rather than string
// surgery:
//
//   - archiveLevelSupported and archive_level is absent: set it to 0
//     (auto-GC stays enabled), creating the auto_gc_behavior block if
//     necessary but preserving any existing sibling fields on it.
//   - !archiveLevelSupported and archive_level is present: clear only
//     archive_level, so a dolt downgrade cannot hard-fail on
//     yaml.UnmarshalStrict seeing a key its own older YAMLConfig struct
//     does not have. The block itself is only removed if doing so leaves
//     it entirely empty (no enable, no incremental_file_size either).
//
// Deliberately inspects only ArchiveLevel_, not "the whole block is
// present" — an earlier version of this function tested
// cfg.BehaviorConfig.AutoGCBehavior != nil, which meant a block carrying
// only a user-set "enable" (no archive_level) both failed to gain
// archive_level when supported, and lost its "enable" setting entirely
// when the whole block was stripped on an unsupported binary.
//
// Returns the rewritten bytes (with the marker re-prepended) and whether a
// rewrite is actually needed; callers should skip the write when unchanged.
func reconcileManagedProxiedServerConfig(body []byte, archiveLevelSupported bool) ([]byte, bool, error) {
	cfg, err := servercfg.NewYamlConfig(body)
	if err != nil {
		return nil, false, fmt.Errorf("parse: %w", err)
	}

	gc := cfg.BehaviorConfig.AutoGCBehavior
	hasArchiveLevel := gc != nil && gc.ArchiveLevel_ != nil
	changed := false

	switch {
	case archiveLevelSupported && !hasArchiveLevel:
		if gc == nil {
			gc = &servercfg.AutoGCBehaviorYAMLConfig{}
			cfg.BehaviorConfig.AutoGCBehavior = gc
		}
		archiveLevel := 0
		gc.ArchiveLevel_ = &archiveLevel
		changed = true
	case !archiveLevelSupported && hasArchiveLevel:
		gc.ArchiveLevel_ = nil
		if isAutoGCBehaviorEmpty(gc) {
			cfg.BehaviorConfig.AutoGCBehavior = nil
		}
		changed = true
	}

	if !changed {
		return body, false, nil
	}

	rendered, err := yaml.Marshal(cfg)
	if err != nil {
		return nil, false, fmt.Errorf("render: %w", err)
	}
	return append([]byte(managedProxiedServerConfigMarker), rendered...), true, nil
}

// isAutoGCBehaviorEmpty reports whether gc has no fields set at all, i.e.
// it is now safe to omit the whole auto_gc_behavior block rather than
// render an empty "{}" or "auto_gc_behavior:" with no children.
func isAutoGCBehaviorEmpty(gc *servercfg.AutoGCBehaviorYAMLConfig) bool {
	return gc.Enable_ == nil && gc.ArchiveLevel_ == nil && gc.IncrementalFileSize_ == nil
}

// warnUnmanagedProxiedServerConfig emits a one-line notice for a config.yaml
// Beads never rewrites (no managedProxiedServerConfigMarker: a user-custom
// path, or a default-path file predating this marker). It surfaces the same
// two risks reconcileManagedProxiedServerConfig fixes automatically for a
// Beads-owned config, without ever touching the file:
//   - archiveLevelSupported and archive_level is absent: zstd auto-GC may
//     still be active; tell the user how to opt in.
//   - !archiveLevelSupported and archive_level is present: the resolved
//     external dolt may refuse to start on this file (yaml.UnmarshalStrict
//     on an unrecognized key).
//
// Deliberately checks only ArchiveLevel_ (via autoGCArchiveLevelIsSet), not
// "the whole auto_gc_behavior block is present" — the same field-vs-block
// distinction reconcileManagedProxiedServerConfig applies. A block carrying
// only a user-set "enable" (no archive_level) would otherwise both miss the
// zstd-risk warning on a capable binary, and get a false
// refuse-to-start warning on an incapable one.
//
// cfg must be the ALREADY-PARSED config (via servercfg.YamlConfigFromFile,
// not NewYamlConfig on raw bytes) — callers own an unmanaged file's parse,
// and that parse must be interpolation-aware (see the ensureProxiedServerConfig
// doc comment on why raw-bytes parsing is wrong for these files).
func warnUnmanagedProxiedServerConfig(path string, cfg servercfg.ServerConfig, archiveLevelSupported bool) {
	hasArchiveLevel := autoGCArchiveLevelIsSet(cfg)
	switch {
	case archiveLevelSupported && !hasArchiveLevel:
		fmt.Fprintf(os.Stderr,
			"Warning: %s is not Beads-managed (no marker); zstd auto-GC may still be active. "+
				"To opt in, add 'auto_gc_behavior: {archive_level: 0}' under the top-level 'behavior:' key.\n",
			path)
	case !archiveLevelSupported && hasArchiveLevel:
		fmt.Fprintf(os.Stderr,
			"Warning: %s sets auto_gc_behavior.archive_level, but the resolved external dolt predates support "+
				"for it (need >= Dolt %s); the server may refuse to start. Remove the key or upgrade dolt.\n",
			path, doltserver.MinDoltVersionForArchiveLevelConfig)
	}
}

// autoGCArchiveLevelIsSet reports whether cfg's auto_gc_behavior.archive_level
// key is explicitly set — not just "the auto_gc_behavior block is present"
// (a block can carry only a sibling field like "enable"). Both
// servercfg.YamlConfigFromFile and servercfg.NewYamlConfig always return a
// *servercfg.YAMLConfig underneath the ServerConfig interface in the
// pinned module, so the type assertion below is expected to succeed; if it
// ever doesn't, this falls back to the coarser "block is present" test
// rather than panicking.
func autoGCArchiveLevelIsSet(cfg servercfg.ServerConfig) bool {
	if yc, ok := cfg.(*servercfg.YAMLConfig); ok {
		return yc.BehaviorConfig.AutoGCBehavior != nil && yc.BehaviorConfig.AutoGCBehavior.ArchiveLevel_ != nil
	}
	return cfg.AutoGCBehavior() != nil
}

const (
	proxiedServerConfigName = "config.yaml"
	proxiedServerLogName    = "server.log"
)

func proxiedServerRoot(beadsDir string) string {
	return doltserver.ResolveDoltDir(beadsDir)
}

func proxiedServerConfigPath(beadsDir string) string {
	return filepath.Join(proxiedServerRoot(beadsDir), proxiedServerConfigName)
}

func proxiedServerLogPath(beadsDir string) string {
	return filepath.Join(proxiedServerRoot(beadsDir), proxiedServerLogName)
}

func envOrAbsJoin(envName, beadsDir string) string {
	p := os.Getenv(envName)
	if p == "" {
		return ""
	}
	if filepath.IsAbs(p) {
		return p
	}
	return filepath.Join(beadsDir, p)
}

func resolveProxiedServerRootPath(beadsDir string) (string, error) {
	if p := envOrAbsJoin("BEADS_PROXIED_SERVER_ROOT_PATH", beadsDir); p != "" {
		return p, nil
	}
	info, err := configfile.LoadProxiedServerClientInfo(beadsDir)
	if err != nil {
		return "", err
	}
	if p := info.ResolvedRootPath(beadsDir); p != "" {
		return p, nil
	}
	return proxiedServerRoot(beadsDir), nil
}

func resolveProxiedServerConfigPath(beadsDir string) (path string, isCustom bool, err error) {
	if p := envOrAbsJoin("BEADS_PROXIED_SERVER_CONFIG", beadsDir); p != "" {
		return p, true, nil
	}
	info, err := configfile.LoadProxiedServerClientInfo(beadsDir)
	if err != nil {
		return "", false, err
	}
	if p := info.ResolvedConfigPath(beadsDir); p != "" {
		return p, true, nil
	}
	root, err := resolveProxiedServerRootPath(beadsDir)
	if err != nil {
		return "", false, err
	}
	return filepath.Join(root, proxiedServerConfigName), false, nil
}

func resolveProxiedServerLogPath(beadsDir string) (path string, isCustom bool, err error) {
	if p := envOrAbsJoin("BEADS_PROXIED_SERVER_LOG", beadsDir); p != "" {
		return p, true, nil
	}
	info, err := configfile.LoadProxiedServerClientInfo(beadsDir)
	if err != nil {
		return "", false, err
	}
	if p := info.ResolvedLogPath(beadsDir); p != "" {
		return p, true, nil
	}
	root, err := resolveProxiedServerRootPath(beadsDir)
	if err != nil {
		return "", false, err
	}
	return filepath.Join(root, proxiedServerLogName), false, nil
}

// ensureProxiedServerConfig resolves (creating if needed) the managed
// sql-server config.yaml. archiveLevelSupported gates whether a freshly
// generated config sets auto_gc_behavior.archive_level: 0 (see
// renderProxiedServerConfig).
//
// Parsing an EXISTING file (custom path, or a pre-marker default-path file
// we only warn about — never a Beads-managed one) MUST go through
// servercfg.YamlConfigFromFile, not servercfg.NewYamlConfig on raw bytes:
// YamlConfigFromFile expands ${VAR}-style environment placeholders before
// parsing (servercfg/env_interpolate.go), and a user's custom config is
// entitled to use that syntax the same way `dolt sql-server --config`
// itself would accept it. NewYamlConfig on raw bytes is only safe for a
// Beads-managed file (marker present), because Beads itself never writes
// interpolation syntax into a file it generates.
func ensureProxiedServerConfig(beadsDir string, archiveLevelSupported bool) (string, error) {
	path, isCustom, err := resolveProxiedServerConfigPath(beadsDir)
	if err != nil {
		return "", err
	}

	if isCustom {
		info, err := os.Stat(path)
		if err != nil {
			return "", fmt.Errorf("ensureProxiedServerConfig: custom config %s: %w", path, err)
		}
		if !info.Mode().IsRegular() {
			return "", fmt.Errorf("ensureProxiedServerConfig: custom config %s: not a regular file", path)
		}
		cfg, err := servercfg.YamlConfigFromFile(filesys.LocalFS, path)
		if err != nil {
			return "", fmt.Errorf("ensureProxiedServerConfig: custom config %s: parse: %w", path, err)
		}
		// A custom config is always user-owned, whether or not it happens to
		// carry the marker — never rewritten, only warned about.
		warnUnmanagedProxiedServerConfig(path, cfg, archiveLevelSupported)
		return path, nil
	}

	root := filepath.Dir(path)
	if err := os.MkdirAll(root, config.BeadsDirPerm); err != nil {
		return "", fmt.Errorf("ensureProxiedServerConfig: mkdir %s: %w", root, err)
	}

	switch _, err := os.Stat(path); {
	case err == nil:
		// The marker is a literal comment line Beads itself writes and never
		// interpolates — safe to check on raw bytes before deciding which
		// (interpolation-aware or not) parse path applies below.
		body, err := os.ReadFile(path) //nolint:gosec // G304: path is Beads' own default config.yaml location, not user-request input
		if err != nil {
			return "", fmt.Errorf("ensureProxiedServerConfig: read existing config %s: %w", path, err)
		}
		if isManagedProxiedServerConfig(body) {
			// Beads-managed: never carries interpolation syntax, so raw-bytes
			// parsing (inside reconcileManagedProxiedServerConfig) is safe and
			// avoids a redundant interpolation pass. BUT the marker only
			// proves Beads generated the file originally — an operator can
			// still hand-edit a managed file afterward (e.g. add a ${VAR}
			// placeholder), which the non-interpolating raw parse cannot
			// handle. That must degrade gracefully rather than fail the
			// whole command: dolt itself would still start fine against
			// such a file once its own interpolation pass runs.
			reconciled, changed, rerr := reconcileManagedProxiedServerConfig(body, archiveLevelSupported)
			if rerr != nil {
				fmt.Fprintf(os.Stderr,
					"Warning: %s carries the Beads-managed marker but failed to parse (%v); treating as "+
						"unmanaged (not rewriting). If this file was hand-edited (e.g. with ${VAR} "+
						"placeholders), that's expected — dolt itself will interpolate it at startup.\n",
					path, rerr)
				cfg, cerr := servercfg.YamlConfigFromFile(filesys.LocalFS, path)
				if cerr != nil {
					return "", fmt.Errorf("ensureProxiedServerConfig: existing config %s: parse: %w", path, cerr)
				}
				warnUnmanagedProxiedServerConfig(path, cfg, archiveLevelSupported)
				return path, nil
			}
			if changed {
				// os.Rename (inside atomicWriteFile) does not follow a
				// symlink at its destination — it replaces whatever is AT
				// that path, symlink or not. Resolve to the physical file
				// first so a symlinked managed config is rewritten through
				// the symlink (the symlink itself survives) rather than
				// having the symlink replaced by a regular file.
				if werr := atomicWriteFile(resolveConfigWriteTarget(path), reconciled); werr != nil {
					return "", fmt.Errorf("ensureProxiedServerConfig: rewrite managed config %s: %w", path, werr)
				}
			}
		} else {
			// Pre-marker default-path file (created by a Beads version before
			// this fix) — treat like a custom config: interpolation-aware
			// parse, never rewritten, only warned about.
			cfg, err := servercfg.YamlConfigFromFile(filesys.LocalFS, path)
			if err != nil {
				return "", fmt.Errorf("ensureProxiedServerConfig: existing config %s: parse: %w", path, err)
			}
			warnUnmanagedProxiedServerConfig(path, cfg, archiveLevelSupported)
		}
		return path, nil
	case !os.IsNotExist(err):
		return "", fmt.Errorf("ensureProxiedServerConfig: stat %s: %w", path, err)
	}

	port, err := proxy.PickFreePort()
	if err != nil {
		return "", fmt.Errorf("ensureProxiedServerConfig: pick free port: %w", err)
	}

	if !archiveLevelSupported {
		fmt.Fprintf(os.Stderr, archiveLevelSupportNoticeFormat, doltserver.MinDoltVersionForArchiveLevelConfig)
	}

	body, err := renderProxiedServerConfig(port, archiveLevelSupported)
	if err != nil {
		return "", fmt.Errorf("ensureProxiedServerConfig: render YAML: %w", err)
	}
	// See the reconcile call site above for why the write target is
	// resolved through any symlink first: this branch only runs after
	// os.Stat(path) reported IsNotExist, which is also true for a dangling
	// symlink — resolveConfigWriteTarget falls back to path itself in that
	// case, so a dangling symlink is simply replaced (self-healed) here.
	if err := atomicWriteFile(resolveConfigWriteTarget(path), body); err != nil {
		return "", fmt.Errorf("ensureProxiedServerConfig: write %s: %w", path, err)
	}
	return path, nil
}

// resolveConfigWriteTarget resolves path to its physical location before
// an atomic rewrite. os.Rename's destination argument does not follow
// symlinks — it unlinks and replaces whatever is AT that path, symlink or
// not — so writing straight to a symlinked config.yaml would silently
// replace the symlink itself with a regular file instead of updating the
// file it points at. Falls back to path unresolved when it does not exist
// yet (filepath.EvalSymlinks errors on a missing path), which covers both
// the ordinary "no config.yaml yet" case and a dangling symlink.
func resolveConfigWriteTarget(path string) string {
	resolved, err := filepath.EvalSymlinks(path)
	if err != nil {
		return path
	}
	return resolved
}

// Validators below emit source-neutral errors. Callers wrap with whichever
// label is meaningful at their site: CLI callers prepend the flag name
// (e.g. "--proxied-server-config-path"); runtime callers (uow factory, etc.)
// prepend whatever label fits — the path may have come from env var or
// the proxied_server_client_info.json sidecar, not necessarily a flag.

func validateProxiedServerConfig(path string) error {
	info, err := os.Stat(path)
	if err != nil {
		return fmt.Errorf("%s: %w", path, err)
	}
	if !info.Mode().IsRegular() {
		return fmt.Errorf("%s: not a regular file", path)
	}
	if _, err := servercfg.YamlConfigFromFile(filesys.LocalFS, path); err != nil {
		return fmt.Errorf("%s: parse: %w", path, err)
	}
	return nil
}

func validateProxiedServerRootPath(path string) error {
	switch info, err := os.Stat(path); {
	case err == nil:
		if !info.IsDir() {
			return fmt.Errorf("%s: not a directory", path)
		}
	case !os.IsNotExist(err):
		return fmt.Errorf("%s: %w", path, err)
	}
	return nil
}

func validateProxiedServerLogPath(path string) error {
	parent := filepath.Dir(path)
	parentInfo, err := os.Stat(parent)
	if err != nil {
		return fmt.Errorf("%s: parent directory: %w", path, err)
	}
	if !parentInfo.IsDir() {
		return fmt.Errorf("%s: parent %s is not a directory", path, parent)
	}
	switch info, err := os.Stat(path); {
	case err == nil:
		if !info.Mode().IsRegular() {
			return fmt.Errorf("%s: not a regular file", path)
		}
	case !os.IsNotExist(err):
		return fmt.Errorf("%s: %w", path, err)
	}
	return nil
}

// renderProxiedServerConfig renders the managed sql-server config.yaml. When
// archiveLevelSupported is true it sets auto_gc_behavior.archive_level: 0,
// so this server's background auto-GC writes classic Snappy table files
// instead of zstd archives (gastownhall/beads#4986); auto-GC itself stays
// enabled (the sibling "enable" key is left unset, which Dolt defaults to
// true). When false, the key is omitted entirely — an older external dolt's
// own YAMLConfig struct may not have this field, and Dolt's config loader
// uses yaml.UnmarshalStrict, so an unrecognized key is a hard parse error at
// server startup rather than a silently-ignored one. Callers must gate
// archiveLevelSupported on doltserver.SupportsArchiveLevelConfig.
func renderProxiedServerConfig(port int, archiveLevelSupported bool) ([]byte, error) {
	host := proxiedServerListenerHost
	logLevel := string(servercfg.LogLevel_Info)
	yc := &servercfg.YAMLConfig{
		LogLevelStr: &logLevel,
		ListenerConfig: servercfg.ListenerYAMLConfig{
			HostStr:    &host,
			PortNumber: &port,
		},
	}
	if archiveLevelSupported {
		archiveLevel := 0
		yc.BehaviorConfig.AutoGCBehavior = &servercfg.AutoGCBehaviorYAMLConfig{
			ArchiveLevel_: &archiveLevel,
		}
	}
	body, err := yaml.Marshal(yc)
	if err != nil {
		return nil, err
	}
	return append([]byte(managedProxiedServerConfigMarker), body...), nil
}

const proxiedServerListenerHost = "127.0.0.1"

